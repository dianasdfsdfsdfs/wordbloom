// German vocabulary pipeline (frequency-based).
// Real German words from the hermitdave FrequencyWords list, bucketed into CEFR
// levels by frequency rank, translated DE→RU via MyMemory with Tatoeba examples.
//
// Caveat: the source is a lowercased subtitle-frequency list — no articles /
// genders, and it may include colloquial or inflected forms. Curated German
// (with articles) is merged ahead of these words in mock-words.ts.
//
// Run:  node scripts/build-words-de.mjs   (safe to re-run; resumes from cache)
import { existsSync, readFileSync, writeFileSync } from 'node:fs';

import { STOP } from './de-stopwords.mjs';

const EMAIL = 'vmsinvest@gmail.com';
const N_PER_LEVEL = 400; // words per level, taken in frequency order
const EX_PER_LEVEL = 40;
const DELAY_MS = 200;
const SOURCE = 'https://raw.githubusercontent.com/hermitdave/FrequencyWords/master/content/2018/de/de_50k.txt';
const CACHE_PATH = new URL('./de-translations-cache.json', import.meta.url);
const EX_CACHE_PATH = new URL('./de-examples-cache.json', import.meta.url);
const OUT_PATH = new URL('../src/data/words-de.generated.ts', import.meta.url);
const LEVELS = ['A1', 'A2', 'B1', 'B2', 'C1', 'C2'];

const sleep = (ms) => new Promise((r) => setTimeout(r, ms));
const eligible = (w) => /^[a-zäöüß][a-zäöüß'-]{2,23}$/.test(w) && !STOP.has(w);
const slug = (s) =>
  s
    .toLowerCase()
    .replace(/ä/g, 'ae')
    .replace(/ö/g, 'oe')
    .replace(/ü/g, 'ue')
    .replace(/ß/g, 'ss')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-|-$/g, '');

function clean(t) {
  let s = String(t).trim();
  s = s.replace(/^[\s\-–—•.,;:"'`(){}\[\]]+/, '');
  s = s.replace(/[\s.…\-–—]+$/, '');
  s = s.replace(/\s+/g, ' ').trim();
  return s ? s.charAt(0).toLowerCase() + s.slice(1) : s;
}
function isGood(s) {
  if (!s || !/[а-яё]/i.test(s)) return false;
  if (/[a-z]/i.test(s)) return false;
  if (/[:/]/.test(s) || s.includes('...') || s.includes('…')) return false;
  if (s.length > 36 || s.split(/\s+/).length > 4) return false;
  return true;
}

const cache = existsSync(CACHE_PATH) ? JSON.parse(readFileSync(CACHE_PATH, 'utf8')) : {};
const saveCache = () => writeFileSync(CACHE_PATH, JSON.stringify(cache));
const exCache = existsSync(EX_CACHE_PATH) ? JSON.parse(readFileSync(EX_CACHE_PATH, 'utf8')) : {};
const saveExCache = () => writeFileSync(EX_CACHE_PATH, JSON.stringify(exCache));

const QUOTA = Symbol('quota');

async function translate(word) {
  if (cache[word] !== undefined) return cache[word];
  const url = `https://api.mymemory.translated.net/get?q=${encodeURIComponent(word)}&langpair=de|ru&de=${encodeURIComponent(EMAIL)}`;
  for (let attempt = 0; attempt < 2; attempt++) {
    try {
      const res = await fetch(url);
      const j = await res.json();
      const raw = String(j?.responseData?.translatedText ?? '');
      if (j?.responseStatus === 429 || /MYMEMORY WARNING|QUOTA|USAGE LIMIT|too many/i.test(raw)) return QUOTA;
      const t = raw.trim();
      await sleep(DELAY_MS);
      if (t && /[а-яё]/i.test(t) && t.toLowerCase() !== word.toLowerCase()) {
        cache[word] = t;
        return t;
      }
      return null;
    } catch {
      await sleep(600);
    }
  }
  return null;
}

async function fetchExample(word) {
  if (exCache[word] !== undefined) return exCache[word];
  const url = `https://tatoeba.org/en/api_v0/search?from=deu&to=rus&query=${encodeURIComponent(word)}&sort=relevance&limit=8`;
  let picked = null;
  try {
    const j = await (await fetch(url)).json();
    for (const s of j.results || []) {
      const de = String(s.text || '').trim();
      const tr = (s.translations || []).flat().find((t) => t && t.lang === 'rus');
      const ru = tr ? String(tr.text || '').trim() : '';
      if (!de || !ru || de.length < 8 || de.length > 64) continue;
      if (!de.toLowerCase().includes(word) || !/[а-яё]/i.test(ru)) continue;
      picked = { de, ru };
      break;
    }
  } catch {
    /* ignore */
  }
  if (picked) exCache[word] = picked;
  await sleep(350);
  return picked;
}

function writeOut(words) {
  const out =
    `// AUTO-GENERATED by scripts/build-words-de.mjs — do not edit by hand.\n` +
    `// German vocabulary from the hermitdave FrequencyWords list (MIT),\n` +
    `// bucketed by frequency; Russian via MyMemory, examples via Tatoeba.\n` +
    `import type { Word } from '@/types/domain';\n\n` +
    `// Stored as a JSON string parsed at load (avoids the TS2590 union blow-up).\n` +
    `export const GENERATED_DE: Word[] = JSON.parse(\n  ${JSON.stringify(JSON.stringify(words))},\n);\n`;
  writeFileSync(OUT_PATH, out);
}

async function main() {
  const text = await (await fetch(SOURCE)).text();
  const lines = text.split(/\r?\n/);
  const seen = new Set();
  const selected = [];
  for (const line of lines) {
    const w = line.split(' ')[0];
    if (!w || seen.has(w) || !eligible(w)) continue;
    seen.add(w);
    selected.push({ word: w, level: LEVELS[Math.floor(selected.length / N_PER_LEVEL)] });
    if (selected.length >= LEVELS.length * N_PER_LEVEL) break;
  }
  console.log(`selected ${selected.length} German words (${N_PER_LEVEL}/level)`);

  const words = [];
  const exCount = Object.fromEntries(LEVELS.map((l) => [l, 0]));
  let done = 0;
  let skipped = 0;
  let quotaHit = false;

  for (const r of selected) {
    const ru = await translate(r.word);
    if (ru === QUOTA) {
      console.log(`translation quota reached — stopping at ${done}/${selected.length}`);
      quotaHit = true;
      break;
    }
    done++;
    if (done % 100 === 0) {
      console.log(`processed ${done}/${selected.length} (kept ${words.length}, skipped ${skipped})`);
      saveCache();
      saveExCache();
      if (done % 500 === 0) writeOut(words);
    }
    const ru2 = ru ? clean(ru) : '';
    if (!isGood(ru2)) {
      skipped++;
      continue;
    }
    const word = {
      id: `de-${r.level.toLowerCase()}-${slug(r.word)}`,
      lang: 'de',
      level: r.level,
      headword: r.word,
      pos: 'other',
      translations: { ru: ru2 },
    };
    if (exCount[r.level] < EX_PER_LEVEL) {
      const ex = await fetchExample(r.word);
      if (ex) {
        word.example = ex.de;
        word.exampleTranslation = ex.ru;
        exCount[r.level]++;
      }
    }
    words.push(word);
  }
  saveCache();
  saveExCache();
  writeOut(words);
  console.log(`WROTE ${words.length} German words (skipped ${skipped}${quotaHit ? ', quota-limited — re-run to continue' : ''}) -> src/data/words-de.generated.ts`);
}

main();
